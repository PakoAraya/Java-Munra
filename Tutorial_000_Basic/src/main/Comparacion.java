package main;

public class Comparacion {
  public static void main(String[] args) {
    if (8 == 9) {
      System.out.println("8 es igual a 9");
    }
    if (9 < 4) {
      System.out.println("9 es menor que 4");
    }
    if (0 > -4) {
      System.out.println("0 es mayor que -4");
    }
    if (9 <= 20) {
      System.out.println("9 es menor o igual que 20");
    }
    if (3 >= 6) {
      System.out.println("3 es mayor o igual que 6");
    }
    if (4 != 4) {
      System.out.println("4 no es igual a 4");
    }
  }
}
