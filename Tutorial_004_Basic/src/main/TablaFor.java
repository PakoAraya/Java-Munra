package main;

import java.util.Iterator;

public class TablaFor {
	public static void main(String[] args) {
		int tabla7 = 7, resultado;
		for (int i=0; i <= 10; i++) {
			resultado = tabla7 * i;
			System.out.println("El resultado de 7 x " + " es: " + resultado );
		}
	System.out.println("Process Completed!");
	}
}
